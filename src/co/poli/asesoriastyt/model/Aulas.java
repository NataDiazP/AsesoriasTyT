package co.poli.asesoriastyt.model;

public class Aulas {
	
	
	private int idAula;
	private String descripcion;
	private int tipoAula;
	private int encargadoCorreo;
	
	
	public int getIdAula() {
		return idAula;
	}
	public void setIdAula(int idAula) {
		this.idAula = idAula;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getTipoAula() {
		return tipoAula;
	}
	public void setTipoAula(int tipoAula) {
		this.tipoAula = tipoAula;
	}
	public int getencargadoCorreo() {
		return encargadoCorreo;
	}
	public void setencargadoCorreo(int encargadoCorreo) {
		this.encargadoCorreo = encargadoCorreo;
	}
	
	

}
